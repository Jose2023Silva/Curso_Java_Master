package org.master.java.Variables;

public class TipoFloat {
    public static void main(String[] args) {
        float realFloat = 1;
        System.out.println(realFloat);
        float realFloat2 = 1.1f;
        System.out.println("realFloat2 = " + realFloat2);

        float noExponente = 22.2532E2f; // 22.2532e2f
        System.out.println("noExponente = " + noExponente);

    }
}
